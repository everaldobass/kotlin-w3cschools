package fundamentos

fun main() {

    // Variáveis são contêineres para armazenar valores de dados.
    //Para criar uma variável, use var ou val e atribua um valor a ela com o sinal de igual ( =):
    // Não precisa declarar o tipo da variavel

    var nome = "Everaldo"

    //Quando você cria uma variável com a valpalavra - chave, o valor não pode ser alterado / reatribuído.
    val idade = 40

    println("Seu nome: "+ nome)
    println("Seu nome: " + idade)


}