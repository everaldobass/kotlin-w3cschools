package fundamentos

// Operadores Kotlin

fun main() {

    //Operadores de atribuição
    println("Atribuição: == ")

    //Operadores aritméticos
    println("Adição: + ")
    println("Subtração: - ")
    println("Multiplicação: * ")
    println("Divisão: / ")
    println("Modulos: % ")
    println("Incremento: ++ ")
    println("Decremento: -- ")

    //Operadores de comparação
    println("Igual: == ")
    println("Diferente: != ")
    println("Maior: > ")
    println("Menor: < ")
    println("Maior ou igual: >= ")
    println("Menor ou igual: <= ")

    //Operadores lógicos
    println(" And: && ")
    println(" Or: || ")
    println(" Negação: ! ")

}